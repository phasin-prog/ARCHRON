-- =========================================================
-- TRIGGER FUNCTION: auto row_id / row_i / row_code
-- =========================================================
create or replace function public.entries_auto_row_code()
returns trigger language plpgsql as $$
declare
  prefix text;
  next_i integer;
begin
  if new.row_i is not null and new.row_code is not null then
    return new;
  end if;

  if new.row_id is null then
    new.row_id := gen_random_uuid();
  end if;

  prefix := case new.content_type
    when 'article'      then 'ART'
    when 'concept'      then 'CON'
    when 'reading-set'  then 'RDS'
    when 'source-note'  then 'SRC'
    when 'person'       then 'THK'
    when 'book'         then 'BOK'
    when 'school'       then 'SCH'
    when 'symbol'       then 'SYM'
    when 'term'         then 'TRM'
    else 'ENT'
  end;

  select coalesce(max(row_i), 0) + 1 into next_i
  from public.entries
  where content_type = new.content_type;

  new.row_i    := next_i;
  new.row_code := prefix || '-' || lpad(next_i::text, 3, '0');
  new.row_name := coalesce(new.row_name, new.title);

  return new;
end;
$$;

-- =========================================================
-- TRIGGER FUNCTION: library FTS
-- =========================================================
create or replace function public.library_fts_trigger()
returns trigger language plpgsql as $$
begin
  new.fts :=
    setweight(to_tsvector('simple', coalesce(new.title,'')), 'A')
    || setweight(to_tsvector('simple', coalesce(new.slug,'')), 'B')
    || setweight(to_tsvector('simple', coalesce(array_to_string(new.tags,' '),'')), 'B')
    || setweight(to_tsvector('simple', coalesce(new.summary,'')), 'C');
  return new;
end;
$$;

-- =========================================================
-- TRIGGER FUNCTION: chunks FTS
-- =========================================================
create or replace function public.chunks_fts_trigger()
returns trigger language plpgsql as $$
begin
  new.fts :=
    setweight(to_tsvector('simple', coalesce(new.heading,'')), 'B')
    || setweight(to_tsvector('simple', coalesce(new.summary,'')), 'C');
  return new;
end;
$$;

