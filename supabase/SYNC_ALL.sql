-- =========================================================
-- BACKFILL — สร้าง row_id/row_i/row_code ให้ row เก่า
-- =========================================================
do $$
declare
  r record;
  prefix text;
  counter integer;
  ct record;
begin
  for ct in
    select distinct content_type from public.entries where row_i is null order by content_type
  loop
    counter := 0;
    prefix := case ct.content_type
      when 'article'      then 'ART'
      when 'concept'      then 'CON'
      when 'reading-set'  then 'RDS'
      when 'source-note'  then 'SRC'
      when 'person'       then 'THK'
      when 'book'         then 'BOK'
      when 'school'       then 'SCH'
      when 'symbol'       then 'SYM'
      when 'term'         then 'TRM'
      else 'ENT'
    end;

    for r in
      select id, title from public.entries
      where content_type = ct.content_type and row_i is null
      order by created_at
    loop
      counter := counter + 1;
      update public.entries
      set row_id   = coalesce(row_id, gen_random_uuid()),
          row_i    = counter,
          row_code = prefix || '-' || lpad(counter::text, 3, '0'),
          row_name = coalesce(row_name, title)
      where id = r.id;
    end loop;
  end loop;
end;
$$;

-- =========================================================
-- RPC: get_comment_subtree — recursive CTE for reply threads
-- =========================================================
create or replace function get_comment_subtree(root_id text)
returns setof public.comments as $$
  with recursive tree as (
    select * from public.comments where id = root_id and status = 'visible'
    union all
    select c.* from public.comments c
    join tree t on c.parent_id = t.id
    where c.status = 'visible'
  )
  select * from tree;
$$ language sql stable;

