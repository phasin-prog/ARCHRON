-- increment page view (+1)
create or replace function public.increment_page_view(p_slug text)
returns bigint
language plpgsql
security definer
set search_path = public
as $$
declare
  new_views bigint;
begin
  insert into public.page_views (slug, views, updated_at)
  values (p_slug, 1, now())
  on conflict (slug)
  do update set views = public.page_views.views + 1, updated_at = now()
  returning views into new_views;
  return new_views;
end; $$;

-- total page views
create or replace function public.total_page_views()
returns bigint
language sql
security definer
set search_path = public
as $$
  select coalesce(sum(views), 0)::bigint from public.page_views;
$$;

-- search library FTS
create or replace function public.search_library_fts(
  p_q text,
  p_limit integer default 5
)
returns setof public.library
language sql stable
set search_path = public
as $$
  select * from public.library
  where fts @@ plainto_tsquery('simple', p_q)
  order by ts_rank(fts, plainto_tsquery('simple', p_q)) desc
  limit p_limit;
$$;

